#converting float into integer:
a=33.66
b=int(a)
print(b,type(b))  #33 <class 'int'>


#converting float into bool:possible

print(bool(33.66)) #33 <class 'int'> True
print(0.0)  #false

#Note :-for 0.0 it returns false and for all other non zero floating print numbers it returns true


#converting float into str:possible
print(str(33.66)) # 33.66

#while converting float into string single or double quotes for values wont be desplayed in the output

#converting float into complex:possible
print(comples(33.66))  #33.66+0j
print(complex(0.0))  #0j


